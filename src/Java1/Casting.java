package Java1;

public class Casting {
    public static void main(String[]args){
        int num=9;
        double num1=num;
        System.out.println(num);
        System.out.println(num1);
        double num2=9.999;
        int num3=(int)num2;
        System.out.println(num2);
        System.out.println(num3);


    }
}
